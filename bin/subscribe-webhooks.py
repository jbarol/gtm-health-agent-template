#!/usr/bin/env python3
"""Plan #44 Task #14 — manage Anthropic webhook subscriptions.

This script subscribes the GTM Health Agent's Anthropic workspace to the
four webhook event classes the orchestrator listens for. Run it once at
deploy time; re-run any time the subscription list changes.

Usage:

    # Show what would happen (default)
    python bin/subscribe-webhooks.py --dry-run

    # Actually create the subscriptions
    python bin/subscribe-webhooks.py --apply

    # Tear them down (e.g. before a workspace migration)
    python bin/subscribe-webhooks.py --delete

Runbook: docs/runbooks/managed-agents-conformance.md (created by Plan
#44 Task #1, Bundle A).

The webhook target URL is read from the ``ANTHROPIC_WEBHOOK_URL`` env
var. Set it to your Railway public URL + ``/webhooks/anthropic`` (the
path defined in :mod:`orchestrator.anthropic_webhooks_register`).

The signing key is generated by Anthropic on subscription create and
returned in the response. Save it to ``ANTHROPIC_WEBHOOK_SIGNING_KEY``
on Railway — the orchestrator reads it at every request to verify
signatures.

Exit codes:
    0 — succeeded (or dry-run completed)
    1 — usage error / argparse rejection
    2 — Anthropic API rejected the request (creds, quota, etc.)
"""

from __future__ import annotations

import argparse
import os
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent

# Event types subscribed for the orchestrator's webhook handler. Keep in
# sync with ``orchestrator/anthropic_webhooks.py:_HANDLERS``.
SUBSCRIBED_EVENT_TYPES = [
    "session.status_terminated",
    "session.thread_terminated",
    "vault_credential.refresh_failed",
    "session.outcome_evaluation_ended",
]


def _load_env() -> None:
    """Manual dotenv loader matching ``orchestrator/config.py``."""
    dotenv = REPO_ROOT / ".env"
    if not dotenv.exists():
        return
    for line in dotenv.read_text().splitlines():
        line = line.strip()
        if line and not line.startswith("#") and "=" in line:
            k, _, v = line.partition("=")
            os.environ.setdefault(k.strip(), v.strip())


def _build_client():
    """Return an Anthropic client. Lazy import so ``--help`` works
    without the SDK installed (e.g. during onboarding doc reads)."""
    import anthropic  # type: ignore

    admin_key = os.environ.get("ANTHROPIC_ADMIN_KEY", "")
    if admin_key:
        return anthropic.Anthropic(api_key=admin_key)
    return anthropic.Anthropic()


def _webhooks_resource(client):
    """Return the SDK's webhooks resource.

    Lives at ``client.beta.webhooks`` as of anthropic-python 0.100.x —
    NOT at the top level (closing-review HIGH #1, 2026-05-13). This
    helper centralizes that detail so a future SDK move (e.g. promotion
    out of beta) is a one-line change.

    NOTE: as of this writing the SDK exposes only ``unwrap`` on this
    resource. ``create``/``list``/``delete`` are not part of the public
    SDK surface and webhook subscriptions are managed via the web
    console; this script attempts the SDK calls and falls back to a
    clear error so the operator knows to use the console.
    """
    return client.beta.webhooks


def _list_existing(client) -> list:
    """List currently-subscribed webhooks. Returns [] on any failure
    so callers can still see the dry-run plan."""
    try:
        webhooks = _webhooks_resource(client)
        page = webhooks.list()
        if hasattr(page, "data"):
            return list(page.data)
        if isinstance(page, list):
            return page
        return list(page)
    except AttributeError as e:
        print(
            f"[WARN] beta.webhooks.list() not available on this SDK "
            f"version ({e}). Manage subscriptions via the Anthropic "
            f"console: https://console.anthropic.com/settings/webhooks",
            file=sys.stderr,
        )
        return []
    except Exception as e:
        print(f"[WARN] beta.webhooks.list() failed: {e}", file=sys.stderr)
        return []


def _subscribe(client, target_url: str, apply: bool) -> int:
    """Subscribe to every event type. Returns the count actually created
    (dry-run: pretend-count). Idempotent — existing subscriptions for the
    same (target_url, event_type) pair are kept, not duplicated."""
    existing = _list_existing(client) if apply else []
    existing_keys = set()
    for sub in existing:
        url = getattr(sub, "url", None) or (
            sub.get("url", "") if isinstance(sub, dict) else ""
        )
        events = getattr(sub, "events", None) or (
            sub.get("events", []) if isinstance(sub, dict) else []
        )
        for ev in events or []:
            existing_keys.add((url, ev))

    created = 0
    for ev in SUBSCRIBED_EVENT_TYPES:
        if (target_url, ev) in existing_keys:
            print(f"[SKIP] already subscribed: {ev} → {target_url}")
            continue
        if not apply:
            print(f"[DRY] would subscribe: {ev} → {target_url}")
            created += 1
            continue
        try:
            webhooks = _webhooks_resource(client)
            resp = webhooks.create(url=target_url, events=[ev])
            signing_key = getattr(resp, "signing_key", "") or (
                resp.get("signing_key", "") if isinstance(resp, dict) else ""
            )
            print(f"[OK]  subscribed: {ev} → {target_url}")
            if signing_key:
                print(
                    f"[KEY] signing key (set ANTHROPIC_WEBHOOK_SIGNING_KEY):\n"
                    f"      {signing_key}"
                )
            created += 1
        except AttributeError as e:
            print(
                f"[ERR] beta.webhooks.create() not available on this SDK "
                f"version ({e}). Manage subscriptions via the Anthropic "
                f"console: https://console.anthropic.com/settings/webhooks",
                file=sys.stderr,
            )
        except Exception as e:
            print(f"[ERR] subscribe {ev} → {target_url}: {e}", file=sys.stderr)
    return created


def _delete(client, target_url: str, apply: bool) -> int:
    """Delete every subscription matching ``target_url`` and one of our
    event types. Returns the count actually removed."""
    existing = _list_existing(client)
    removed = 0
    for sub in existing:
        url = getattr(sub, "url", None) or (
            sub.get("url", "") if isinstance(sub, dict) else ""
        )
        sub_id = getattr(sub, "id", None) or (
            sub.get("id", "") if isinstance(sub, dict) else ""
        )
        events = getattr(sub, "events", None) or (
            sub.get("events", []) if isinstance(sub, dict) else []
        )
        match_events = [ev for ev in (events or []) if ev in SUBSCRIBED_EVENT_TYPES]
        if not match_events:
            continue
        if target_url and url != target_url:
            continue
        if not apply:
            print(f"[DRY] would delete: id={sub_id} url={url} events={match_events}")
            removed += 1
            continue
        try:
            webhooks = _webhooks_resource(client)
            webhooks.delete(sub_id)
            print(f"[OK]  deleted: id={sub_id} url={url}")
            removed += 1
        except AttributeError as e:
            print(
                f"[ERR] beta.webhooks.delete() not available on this SDK "
                f"version ({e}). Manage subscriptions via the Anthropic "
                f"console: https://console.anthropic.com/settings/webhooks",
                file=sys.stderr,
            )
        except Exception as e:
            print(f"[ERR] delete {sub_id}: {e}", file=sys.stderr)
    return removed


def _print_next_steps(apply: bool, deleted: bool) -> None:
    """Final user-facing summary. Mirrors ``bin/rollback-agent.py`` so
    operators see consistent UX from every Plan #44 script."""
    print("")
    print("─" * 60)
    print("Next steps:")
    if deleted:
        print("  - Subscriptions removed. The orchestrator's webhook")
        print("    handler will return 400 (invalid signature) on any")
        print("    re-delivery from Anthropic.")
        print("  - To re-subscribe: rerun this script with --apply")
        return
    if not apply:
        print("  - Re-run with --apply to actually create the subscriptions.")
        print("  - Set ANTHROPIC_WEBHOOK_URL to your Railway URL + ")
        print("    `/webhooks/anthropic`.")
        return
    print("  - Copy the printed signing key into Railway as")
    print("    ANTHROPIC_WEBHOOK_SIGNING_KEY. The orchestrator reads it")
    print("    on every request to verify the HMAC signature.")
    print("  - Verify the orchestrator's /health endpoint reports the")
    print("    new BUILD_COMMIT matching this deploy.")
    print("  - Runbook: docs/runbooks/managed-agents-conformance.md")
    print("─" * 60)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="subscribe-webhooks.py",
        description=(
            "Manage Anthropic webhook subscriptions for the GTM Health "
            "Agent. See docs/runbooks/managed-agents-conformance.md."
        ),
        epilog=(
            "Examples:\n"
            "  bin/subscribe-webhooks.py --dry-run\n"
            "  bin/subscribe-webhooks.py --apply\n"
            "  bin/subscribe-webhooks.py --delete --apply\n"
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        default=True,
        help="Print the plan without calling Anthropic (default).",
    )
    parser.add_argument(
        "--apply",
        action="store_true",
        help="Actually create/delete subscriptions on Anthropic.",
    )
    parser.add_argument(
        "--delete",
        action="store_true",
        help="Remove existing subscriptions matching this target URL.",
    )
    parser.add_argument(
        "--target-url",
        default=os.environ.get("ANTHROPIC_WEBHOOK_URL", ""),
        help=(
            "The public webhook URL (defaults to env "
            "ANTHROPIC_WEBHOOK_URL). Set to your Railway URL + "
            "'/webhooks/anthropic'."
        ),
    )
    args = parser.parse_args(argv)

    _load_env()

    # --apply explicitly disables --dry-run.
    apply = bool(args.apply)
    target_url = args.target_url

    if not target_url:
        print(
            "[ERR] ANTHROPIC_WEBHOOK_URL not set — pass --target-url or "
            "set the env var.",
            file=sys.stderr,
        )
        return 1

    try:
        client = _build_client()
    except Exception as e:
        print(f"[ERR] failed to build Anthropic client: {e}", file=sys.stderr)
        return 2

    try:
        if args.delete:
            count = _delete(client, target_url, apply)
            print(
                f"\n{count} subscription(s) {'deleted' if apply else 'planned for deletion'}."
            )
        else:
            count = _subscribe(client, target_url, apply)
            print(f"\n{count} subscription(s) {'created' if apply else 'planned'}.")
    except Exception as e:
        print(f"[ERR] subscription op failed: {e}", file=sys.stderr)
        return 2

    _print_next_steps(apply=apply, deleted=args.delete)
    return 0


if __name__ == "__main__":
    sys.exit(main())
